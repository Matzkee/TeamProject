package com.nct.cla;

public class TestResult {
	private String User_Id;
	private String Car_Reg;
	private String Alignment;
	private String Suspension;
	private String Brakes;
	private String Exhaust_Emission;
	private String Head_Lights;
	private String Test_data;
	public String getUser_Id() {
		return User_Id;
	}
	public void setUser_Id(String user_Id) {
		User_Id = user_Id;
	}
	public String getCar_Reg() {
		return Car_Reg;
	}
	public void setCar_Reg(String car_Reg) {
		Car_Reg = car_Reg;
	}
	public String getAlignment() {
		return Alignment;
	}
	public void setAlignment(String alignment) {
		Alignment = alignment;
	}
	public String getSuspension() {
		return Suspension;
	}
	public void setSuspension(String suspension) {
		Suspension = suspension;
	}
	public String getBrakes() {
		return Brakes;
	}
	public void setBrakes(String brakes) {
		Brakes = brakes;
	}
	public String getExhaust_Emission() {
		return Exhaust_Emission;
	}
	public void setExhaust_Emission(String exhaust_Emission) {
		Exhaust_Emission = exhaust_Emission;
	}
	public String getHead_Lights() {
		return Head_Lights;
	}
	public void setHead_Lights(String head_Lights) {
		Head_Lights = head_Lights;
	}
	public String getTest_data() {
		return Test_data;
	}
	public void setTest_data(String test_data) {
		Test_data = test_data;
	}
}
